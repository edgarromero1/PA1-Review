	private void _test(String[] values, String expected) {
		final ByteArrayOutputStream outContent = new ByteArrayOutputStream();
		
		final String input = String.join(" ", values);
		
		System.setIn(new ByteArrayInputStream(input.getBytes()));
		System.setOut(new PrintStream(outContent));
		try {
			E1b.main(new String[] { "foo" });
		} catch (ExitException e) {}
		
		assertEquals(expected, outContent.toString());
		
		System.setIn(null);
		System.setOut(null);
	}
	
	private void _testIsPrime(int n, boolean expected) {
		Boolean result = null;
		try {
			result = Prime.isPrime(n);
		} catch (ExitException e) {}
		assertEquals(expected, (boolean) result);
	}
	
	private void _testPrimeAfter(int n, int expected) {
		Integer result = null;
		try {
			result = Prime.primeAfter(n);
		} catch (ExitException e) {}
		assertEquals(expected, (int) result);
	}
	
	private void _testPrime(int seed, int[] expected) {
		boolean done = false;
		try {
			Prime.resetSequence(seed);
			for (int i=1; i<=expected.length; i++) {
				final Prime p = new Prime();
				assertNotNull(p);
				assertEquals(p.getSequenceSeed(), seed);
				assertEquals(p.getSequenceNumber(), i);
				assertEquals(p.getValue(), expected[i-1]);
			}
			done = true;
		} catch (ExitException e) {}
		assertTrue(done);
	}
	
